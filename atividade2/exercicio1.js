  // Declaração das variáveis numéricas
let num_A = 20;
let num_B = 5;

// Adição
let resultadoSoma = num_A + num_B;
console.log(`O resultado da adição entre ${num_A} e ${num_B}: ${resultadoSoma}`);

// Subtração
let resultadoSubtracao = num_A - num_B;
console.log(`O resultado da subtração entre ${num_A} e ${num_B}: ${resultadoSubtracao}`);

// Multiplicação
let resultadoMultiplicacao = num_A * num_B;
console.log(`O resultado da multiplicação entre ${num_A} e ${num_B}: ${resultadoMultiplicacao}`);

// Divisão
let resultadoDivisao = num_A / num_B;
console.log(`O resultado da divisão entre ${num_A} e ${num_B}: ${resultadoDivisao}`);

// Módulo (Resto da divisão)
let resultadoModulo = num_A % num_B;
console.log(`O resultado do módulo entre ${num_A} e ${num_B}: ${resultadoModulo}`);

